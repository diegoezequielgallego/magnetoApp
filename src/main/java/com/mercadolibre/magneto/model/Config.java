package com.mercadolibre.magneto.model;

public class Config {

	private int operadores;
	private int supervisores;
	private int jefes;
	private int llamadas;
	
	public int getOperadores() {
		return operadores;
	}
	public void setOperadores(int operadores) {
		this.operadores = operadores;
	}
	public int getSupervisores() {
		return supervisores;
	}
	public void setSupervisores(int supervisores) {
		this.supervisores = supervisores;
	}
	public int getJefes() {
		return jefes;
	}
	public void setJefes(int jefes) {
		this.jefes = jefes;
	}
	public int getLlamadas() {
		return llamadas;
	}
	public void setLlamadas(int llamadas) {
		this.llamadas = llamadas;
	}
	
}
